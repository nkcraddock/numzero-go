package main

import (
	"flag"
	"log"
	"net/http"

	"github.com/nkcraddock/numzero"
	"github.com/nkcraddock/numzero/game"
	"github.com/nkcraddock/numzero/server"
)

func main() {
	root := getContentRoot()
	addr := ":3001"
	store := numzero.NewMemoryStore()
	gstore, err := game.NewRedisStore("localhost:6379", "", 0)
	if err != nil {
		panic(err)
	}
	addSomeTestData(gstore)
	c := server.BuildContainer(store, gstore, privateKey, publicKey, root)

	server := &http.Server{Addr: addr, Handler: logMiddleware(c)}
	log.Fatal(server.ListenAndServeTLS("certs/cert.pem", "certs/key.pem"))
}

func logMiddleware(next http.Handler) http.Handler {
	return http.HandlerFunc(func(w http.ResponseWriter, r *http.Request) {
		log.Println("REQUEST", r.Method, r.URL)
		next.ServeHTTP(w, r)
	})
}

func addSomeTestData(store *game.RedisStore) {
	store.FlushDb()
	store.SaveRule(&game.Rule{
		Code:        "build:broke",
		Description: "broke the build",
		Points:      -10,
	})
	store.SaveRule(&game.Rule{
		Code:        "build:fixed",
		Description: "fixed the build",
		Points:      10,
	})
	store.SaveRule(&game.Rule{
		Code:        "build:success",
		Description: "a successful build",
		Points:      1,
	})
	store.SaveRule(&game.Rule{
		Code:        "test:new",
		Description: "added a test",
		Points:      1,
	})
	store.SavePlayer(&game.Player{Name: "Roger"})
	store.SavePlayer(&game.Player{Name: "Chad"})
}

func getContentRoot() string {
	root := flag.String("r", "", "the root path of the client content. blank if bindata")
	flag.Parse()
	return *root
}

